// 한국 시간(UTC+9) 기준 오늘 날짜(YYYY-MM-DD). 빌드 시점에 한 번 계산된다.
function todayInKst(): string {
  const kst = new Date(Date.now() + 9 * 60 * 60 * 1000);
  return kst.toISOString().slice(0, 10);
}

// 예약 발행: publishedAt이 오늘(한국 시간) 이후인 글은 아직 공개하지 않는다.
// 미래 날짜 글은 그 날짜 이후의 재배포(빌드) 때 자동으로 목록에 나타난다.
export const publishedPosts: BlogPost[] = blogPosts.filter(
  (post) => post.publishedAt <= todayInKst(),
);
